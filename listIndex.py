
a = [4, 2, 11, 18, 18, 5, 6, 8, 9]
for i in a:
    if i % 2 == 0 and i % 3 == 0:
      a.remove(i)
print(a)
#运行结果：[4, 2, 11, 18, 5, 8, 9]

a = [4, 2, 11, 18, 18, 5, 6, 8, 9]
b = a[:]
for i in a:
    if i % 2 == 0 and i % 3 == 0:
      b.remove(i)
a = b
print(a)
# 运行结果：[4, 2, 11, 5, 8, 9]


